-- Supabase SQL: developers directory with OTP auth users
create table if not exists public.developers (
  id bigint generated by default as identity primary key,
  user_id uuid unique references auth.users(id) on delete cascade,
  email text,
  location text,
  name text not null,
  slug text not null unique,
  title text not null,
  skills text not null,
  experience_years integer not null default 0,
  github_url text,
  linkedin_url text,
  portfolio_url text,
  bio text,
  profile_image text,
  open_to_work boolean not null default false,
  verified boolean not null default false,
  created_at timestamptz not null default now()
);

create index if not exists developers_slug_idx on public.developers(slug);
create index if not exists developers_open_to_work_idx on public.developers(open_to_work);

alter table public.developers enable row level security;

drop policy if exists "Public read developers" on public.developers;
create policy "Public read developers"
on public.developers for select
using (true);

drop policy if exists "Auth insert own profile" on public.developers;
create policy "Auth insert own profile"
on public.developers for insert
to authenticated
with check (auth.uid() = user_id);

drop policy if exists "Auth update own profile" on public.developers;
create policy "Auth update own profile"
on public.developers for update
to authenticated
using (auth.uid() = user_id)
with check (auth.uid() = user_id);
