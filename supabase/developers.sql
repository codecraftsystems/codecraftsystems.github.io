alter table job_alert_keywords
add constraint fk_job_alert_keywords_user
foreign key (user_id)
references developers(user_id)
on delete cascade;

alter table job_alert_history
add constraint fk_job_alert_history_user
foreign key (user_id)
references developers(user_id)
on delete cascade;


